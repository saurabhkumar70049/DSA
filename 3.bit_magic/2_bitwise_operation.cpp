#include<bits/stdc++.h>
using namespace std;


int main() {
    int a = 1, b = 21;
    cout << "a&b : " << (a&b) << endl;
    cout << "a|b : " << (a|b) << endl;
    cout << "a^b : " << (a^b) << endl;
    cout << "a<<1 : " << (a<<1) << " b << 2 : " << (b<<2) << endl;
    //cout << a ;
    cout << "~a : " << (~a) << " ~b : " << (~b);
    
}